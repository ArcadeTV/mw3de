; =================================================================================================
; VERTICAL INTERRUPT ROUTINE
; =================================================================================================

v_int:
    addq.b  #1,($FFFF8A49).l                ; adopt original instruction
    ori.b   #1,($FFFF8006).w                ; adopt original instruction
    st.b    ($FFFF1980).l
    rte


; -------------------------------------------------------------------------------------------------
; WaitVBlank
; -------------------------------------------------------------------------------------------------

WaitVBlank:
    tst.b  ($FFFF1980).l
    beq    WaitVBlank
    clr.b  ($FFFF1980).l
	rts
